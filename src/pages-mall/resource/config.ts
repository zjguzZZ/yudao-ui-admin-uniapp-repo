/** 获取商城资源配置 */
export function getMallResourceConfig(resource?: string) {
  if (!resource) {
    return undefined
  }
  return mallResourceConfigs[resource]
}

/** 获取商城资源配置列表 */
export function getMallResourceConfigs() {
  return Object.values(mallResourceConfigs)
}

/** 获取分组资源 */
export function getMallGroupResources(group: MallResourceGroup) {
  return group.resources.map(key => mallResourceConfigs[key]).filter(Boolean)
}
