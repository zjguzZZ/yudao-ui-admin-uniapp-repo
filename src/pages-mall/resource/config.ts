/** 本地列表转分页 */
async function createCategoryPage(params: PageParam): Promise<PageResult<Record<string, any>>> {
  const query = { ...params }
  delete query.pageNo
  delete query.pageSize
  const list = await getProductCategoryList(cleanMallQuery(query))
  return createLocalMallPage(
    [...list].sort((a, b) => (a.sort || 0) - (b.sort || 0)),
    params,
  )
}

